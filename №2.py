x,y=0,0
while True:
    print('текущие координаты:', x, y)
    step = input('укажите направление движения (влево, вправо, вверх или вниз):\nдля выхода введите команду стоп.\n')
    if step == 'стоп':
        break
    a = int(input('укажите количество шагов:'))
    if step == 'вправо':
        x+=a
    if step == 'влево':
        x-=a
    if step == 'вниз':
        y-=a
    if step == 'вверх':
        y+=a
    print('текущие координаты:', x, y)